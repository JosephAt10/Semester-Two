class Student {
    String name;
    String studentID;
    int year;
    int midtermScore;
    int finalScore;
    
    public Student(String name, String studentID, int year, int midtermScore, int finalScore) {
        this.name = name;
        this.studentID = studentID;
        this.year = year;
        this.midtermScore = midtermScore;
        this.finalScore = finalScore;
    }
}
